package com.ws.ui.model;

public class User {
private int uid;
private String name,un,ps,addr;


public int getUid() {
	return uid;
}
public void setUid(int uid) {
	this.uid = uid;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getUn() {
	return un;
}
public void setUn(String un) {
	this.un = un;
}
public String getPs() {
	return ps;
}
public void setPs(String ps) {
	this.ps = ps;
}
public String getAddr() {
	return addr;
}
public void setAddr(String addr) {
	this.addr = addr;
}
}
